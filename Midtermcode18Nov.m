% Function for Thermal Conductivity 
%It changes significantly between the solids and liquid phase of material.
%It ensures proper phase trasition crucial for precise thermal modelling
%and adjust diffrerent material properties.
function k = thermalConductivity(region, state)
    T = state.u; % Vector of temperatures at each location
    Ts = 1563;   % Solidus temperature in K
    k = zeros(size(T));
    
    % Apply thermal conductivity based on temperature
    k(T <= Ts) = 5.331 + 0.015 * T(T <= Ts); % Solid phase
    k(T > Ts) = 30.05;                        % Liquid phase
end

% % Function for Specific Heat
% Function for Specific Heat used to calculates temperature-dependent specific heat(Cp) material for phase transition states Parameters:
%Ts (1563 K): The temperature below which the material stays solid.
%Tl (1623 K): The temperature above which the material is fully liquid.
%Lf (227,000 J/kg): The amount of heat needed to change the material from solid to liquid during melting.
% - region: Represents domain (not used here but required for function definition).
% - state.u: A vector of temperatures at each location in the domain.
function Cp = specificHeat(region, state)
    T = state.u; % Vector of temperatures
    Ts = 1563;   % Solidus temperature in K
    Tl = 1623;   % Liquidus temperature in K
    Lf = 227e3;  % Latent heat of fusion in J/kg
    Cp = zeros(size(T));

    % Solid phase
    Cp(T <= Ts) = 338.98 + 0.2437 * T(T <= Ts);

    % Liquid phase
    Cp(T >= Tl) = 735;

    % Phase change region
    between = (T > Ts) & (T < Tl);
    Cp_solid = 338.98 + 0.2437 * Ts;
    Cp_liquid = 735;
    Cp(between) = Cp_solid + ((Cp_liquid - Cp_solid) .* (T(between) - Ts) / (Tl - Ts)) + (Lf / (Tl - Ts));
end

% Laser Heat Source Function
function Qflux = laserHeatSource(region, state)
    P = 195;             % Laser power in W
    beamRadius = 0.01e-3; % Laser beam radius in m
    R = 0.01;            % Reflectivity
    A = pi * (beamRadius^2); % Beam area
    I = P / A;           % Laser intensity (W/m^2)
    scanSpeed = 0.8;   % Scanning speed in m/s

    %Calculate the laser's position along x based on the time
    %Subtracted the -4e-3/2 to get the actual simulated output based on
    %the length

    x_position = -4e-3/2 + state.time * scanSpeed;
    %Computes the X-coordinate of the laser 
    % starting at the midpoint of the domain (-4e-3/2) and advancing based on the elapsed simulation time
    % and the laser scanning speed (`scanSpeed`).

    % Calculate radial distance from the current laser position
    %The radial distance r measures how far a point in the domain is from the laser’s current position (x_position).
    % It is computed using the Euclidean distance formula:
    % Calculate radial distance from the current laser position
    r = sqrt((region.x - x_position).^2 + region.y.^2);

    % Gaussian heat flux distribution for the moving laser models the intensity of the heat flux applied by the laser.
% - The intensity diminishes exponentially as the distance r grows, 
% mimicking the behavior of a real laser where heat is strongest at the center and gradually reduces further out.
    Qflux = I * (1 - R) * exp(-2 * (r.^2) / (beamRadius^2));
end